public class Ifelse {

	public static void main(String[] args) {
		int amountinpocket =10000;
		if (amountinpocket >5000)
		{
			System.out.println("mumbai to goa by car");
		}
		else if(amountinpocket >1000)
		{
		System.out.println("mumbai to khandala by bike");
		}
		else if(amountinpocket >500)
		{
			System.out.println("mumbai to khandala by train");
		}
		else
		{
			System.out.println("local mumbai");
		}
	
		
		System.out.println("end");
		
		
		
	}

}
